                        <span class="blue">リプロスキンはトリプルブースターシステムを採用した導入型柔軟化粧水。</span><br>
                        導入型とは、この後に使う化粧品の美容成分の浸透を高める効果がある化粧水ってことなんですよ！<br>
                        柔軟は読んで字のごとく、肌を柔らかくする化粧水ということです。<br>
                        <span class="blue">水分・油分・うるおいの3つをしっかりと角質層の奥まで浸透させる化粧水です。</span>
                        <br><br>
                        <strong>1. 水分 ： 水溶性美容成分</strong><br>
                        水溶性の美容成分がお肌に一番浸透しやすいのは、お肌が中性になっているとき。でもアナタも知ってる通り、お肌は弱酸性ですよね。そこで、アルカリ性のリプロスキンをつかうことで、お肌のphを一時的に中和して中性にすることで水溶性美容成分を浸透しやすくしてるんです！

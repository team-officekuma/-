                        <strong>2. 油分 ： 油溶性美容成分</strong><br>
                        油溶性の美容成分は、肌に浸透しにくいと言われています。リプロスキンでは水と油をなじませるエマルジョン技術を使った乳化設計で、角質層の奥までしっかりと油溶性の美容成分を届けます。
                        <br><br>
                        <strong>3. うるおいの通り道アクアポリンを活性化</strong><br>
                        お肌をうるおいで満たすためには、水分をいかに角質層に届けるかが重要。そこでポイントとなるのが細胞に存在し、水分の通り道となるアクアポリンです。そしてアクアポリンを活性化させる働きを持つのがアマチャヅル。リプロスキンはアマチャヅルを配合することで、肌のすみずみまで水分をとどけて肌にうるおいを与えやわらかい肌に導きます。

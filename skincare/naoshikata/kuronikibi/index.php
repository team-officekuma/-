                <h2 class="h2" id="ho-mukea">黒ニキビの治し方</h2>
                
                <h4 class="akami-care">黒ニキビには洗顔が最重要！</h4>
                
                <div class="box-2">
                <h4 class="h4">洗顔料の選び方</h4>
                <p>
                洗顔に洗顔料は必要ないともよく言われます。<br>
                実際、洗顔料は少なからず肌に負担がかかります。<br>
                ものによっては洗浄力が強すぎて必要な皮脂まで取り除いてしまい乾燥肌の原因になることもあります。<br>
                <br>
                それらが原因でニキビが出来ていることもあるため、できるだけ肌に優しい天然由来成分で出来た洗顔料を選ぶようにしましょう。<br>
                また、泡立ちのよさも大切です。よく泡立ち、<span class="blue">必要な皮脂や水分を残してくれる洗顔料が理想的</span>です。
                </p>
                    
                <h4 class="h4">保湿ケア</h4>
                <p>
                保湿ケアのポイントは３つ！
                <br><br>
                <strong>・洗顔をした後すぐに保湿！</strong><br>
                洗顔をしたあとは肌が綺麗になっており、皮脂などが落ちた状態にあります。この状態はとても乾燥しやすい状態なので、なるべく早く保湿ケアをしてあげて、肌に水分を与えてあげましょう。
                <br><br>
                <strong>・両手で優しく包むように</strong><br>
                化粧水を塗る時は500円玉程度の大きさになるくらい手に取り、手のひらを使って優しく肌に押し込むようにして塗りましょう。この時擦ってしまったり叩いてしまうと摩擦が肌にダメージを与えてしまうので、上から押さえつけるだけでOKです。手のひらで押さえて肌に染み込ませることで負担を少なくできますよ！
                <br><br>
                <strong>・肌に優しいものを選ぶ</strong><br>
                例えばアルコールが含まれた化粧水などはつけると爽快感があり好んでいる人もいるはずです。ですが、アルオールは余計に乾燥を進めてしまうこともあるので避けた方がいい成分です。他にもサリチル酸のようなピーリング効果のあるものも、場合によっては負担が強く余計にニキビを悪化させてしまうこともあるので注意が必要です。
                <br><br>
                ですので、ビタミンC誘導体やコラーゲン、ヒアルロン酸といった美肌に効果のある成分が含まれたものを選ぶといいですよ！
                </p>    
